/*********************LCD 延时1ms************************************/
void LCD_DLY_ms(unsigned int ms)
{                         
    unsigned int a;
    while(ms)
    {
        a=3200;
        while(a--);
        ms--;
    }
    return;
}

/**************************************************************************************************
 * @fn      halLcd_ConfigIO
 *
 * @brief   Configure IO lines needed for LCD control.
 *
 * @param   None
 *
 * @return  None
 **************************************************************************************************/
static void halLcd_ConfigIO(void)
{
    //#define LCD_CS  	//片选直接接到了GND
    #define LCD_DC     P1_6	//A0  H/L 命令数据选通端，H：数据，L:命令
    #define LCD_SCL    P1_7     //SCLK  时钟 D0（SCLK）
    #define LCD_SDA    P1_5     //SDA   D1（MOSI） 数据
    #define LCD_REST   P1_2     //复位端
    //#define LCD_CS  接地
  
	P1SEL &= ~0xe4;          //让P12，P15，P16 P17为普通IO口
	P1DIR |= 0xe4;           //把P12，P15，P16 P17设置为输出

	LCD_DC=1;
	LCD_SCL=1;
	LCD_SDA=1;
	LCD_REST=1;
}

void LCD_Wrtie_Bus(char dat)
{
    uint8 i=0, temp=0;
    for(i=0;i<8;i++) //发送一个八位数据 
    {
        LCD_SCL=0;  
        temp = dat&0x80;
        if (temp == 0)
        {
            LCD_SDA=0;
        }
        else
        {
            LCD_SDA=1;
        }
        LCD_SCL=1;       
        dat<<=1;    
    }
}

void LCD_WR_DATA8(char dat) //发送数据(8bit的数据)
{
    LCD_DC=1;
    LCD_Wrtie_Bus(dat);
}

void LCD_WR_DATA(int dat)
{
    LCD_DC=1;
    LCD_Wrtie_Bus(dat>>8);
    LCD_Wrtie_Bus(dat);
}

void LCD_WR_REG(uint8 cmd)	 
{
    LCD_DC=0; 
    LCD_Wrtie_Bus(cmd);
}

 void LCD_WR_REG_DATA(uint8 reg,int da)
{
    LCD_WR_REG(reg);
    LCD_WR_DATA(da);
}

void Address_set(unsigned int x1,unsigned int y1,unsigned int x2,unsigned int y2)
{  
   LCD_WR_REG(0x2a);
   LCD_WR_DATA8(x1>>8);
   LCD_WR_DATA8(x1);
   LCD_WR_DATA8(x2>>8);
   LCD_WR_DATA8(x2);
  
   LCD_WR_REG(0x2b);
   LCD_WR_DATA8(y1>>8);
   LCD_WR_DATA8(y1);
   LCD_WR_DATA8(y2>>8);
   LCD_WR_DATA8(y2);

   LCD_WR_REG(0x2C);
}

